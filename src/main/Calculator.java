package main;

public class Calculator {
    public double sum(double x, double y){
        return x + y;
    }
    public double subtraction(double x, double y){
        return x - y;
    }
    public double multiplication(double x, double y){
        return x *y;
    }
    public double division (double x, double y){
        return x / y;
    }
}
